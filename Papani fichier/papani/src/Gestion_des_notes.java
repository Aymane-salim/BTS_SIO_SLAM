    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(JOptionPane.showConfirmDialog(null, "êtes-vous sûr de vouloir supprimer ?",
                    "supprimer etudiant", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION)
                
                if(txtid.getText().length() != 0){
                    stm.executeUpdate("Delete from etudiant where id = "+txtid.getText());
                    afficher();
                }else{
                    JOptionPane.showMessageDialog(null, "veuillez remplir le champ id !");
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erreur de suppression \n"+e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

