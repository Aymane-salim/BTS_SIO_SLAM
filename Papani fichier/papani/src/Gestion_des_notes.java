    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=txtid.getText();
        String nom=txtno.getText();
        String prenom=txtpr.getText();
        String branche=txtbr.getSelectedItem().toString();
        String note=txtnot.getText();
        String requete="insert into etudiant(id,Nom, Prenom, branche, note )VALUES ('"+id+"','"+nom+"','"+prenom+"','"+branche+"','"+note+"')";        
        
        try {
            stm.executeUpdate(requete);
            JOptionPane.showMessageDialog(null, "l'etudiant(e) est bien ajouté(e)");
            txtno.setText("");
            txtpr.setText("");
            txtbr.setSelectedItem(2);
            txtnot.setText("");
            afficher();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

